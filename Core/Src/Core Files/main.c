/**
  * @brief  The application entry point.
  * @retval int
  */
int main(void)
{

  /* Reset of all peripherals, Initialises the Flash interface and the Systick. */
  HAL_Init();

  /* Configure the system clock */
  SystemClock_Config();

  /* Initialise all configured peripherals */
  MX_GPIO_Init();
  MX_DMA_Init();
  MX_ADC1_Init();
  MX_TIM1_Init();
  MX_TIM2_Init();
  MX_TIM3_Init();
  MX_TIM4_Init();
  MX_USART1_UART_Init();
  MX_IWDG_Init();


  if(HAL_TIM_Base_Start_IT(&htim4) != HAL_OK)
   		{
   		   Error_Handler();
   		}

  if(HAL_TIM_Base_Start_IT(&htim2) != HAL_OK)
     		{
     		   Error_Handler();
     		}

  /* USER CODE BEGIN 2 */
  /* Bring up low-level output peripherals before applying any saved
   * user settings.  The order matters: start the PWM timers so that
   * the ring and motor outputs are ready, then restore the persisted
   * user preferences, then configure inputs and sensor capture. */

  actuators_init();    // initialise motor, fan and compressor PWM outputs
  lights_init();       // initialise ring LED PWM before applying brightness
  user_init();         // restore persisted settings and apply initial visuals
  buttons_init();      // set up button debouncers now that GPIOs are ready
  sensors_init();      // start ADC + DMA and input capture timers
  HAL_Delay(10);

  g_ticks_1s = 0;
  inital_start = true;

  /* Initialise the high-level state machine after all peripherals
   * have been configured.  This will set up the estimator and
   * establish initial conditions for the compressor and fans. */
#if ENABLE_CONTROL_FSM
  control_fsm_init();
#else
  /* Control FSM disabled at build time; skip FSM initialisation. */
#endif

  /* Infinite loop */
  /* USER CODE BEGIN WHILE */
  while (1)
  {

    // Refresh watchdog to avoid system reset
    HAL_IWDG_Refresh(&hiwdg);

    /* 1 ms cadence: acquire & filter all sensors */
    if (elapsed_1ms) {
    	update_sensor_data();
    	elapsed_1ms = 0;
    }

    /* 20 ms cadence: control / FSM / debouncing */
    if (g_ticks_20ms) {
      /* run control loops / FSM steps here */
        uint32_t now_ms = HAL_GetTick();
        set_lights_task_20ms(now_ms);
        buttons_task_20ms(now_ms);
        user_settings_task_20ms(now_ms);
#if ENABLE_CONTROL_FSM
        control_fsm_run_tick(now_ms);
#else
        /* Control FSM disabled; skip state machine tick. */
#endif
        g_ticks_20ms = 0;
    }

    /* 500 ms cadence: UI/telemetry heartbeats */
    if (g_ticks_500ms) {
      /* LED heartbeat / light telemetry (no prints here by design) */
      print_data();
      // print_estimated_data();
      g_ticks_500ms = 0;
    }

    /* 1 s cadence: safety / slow checks */
    if (g_ticks_1s) {
      /* e.g., background health checks */
    	if (!inital_start) {
    	//run_fault_detetcion();
    	}
    	g_ticks_1s = 0;
    	inital_start = false;
    }

    /* 30 s cadence: very slow housekeeping */
    if (g_ticks_30s) {
      /* e.g., periodic stats/flash save */
    	g_ticks_30s = 0;
    }
  }
  /* USER CODE END WHILE */
}
